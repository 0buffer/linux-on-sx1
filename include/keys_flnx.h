#define JoyRight	FL_Right
#define JoyUp		FL_Up
#define JoyDown		FL_Down
#define Shift		FL_Shift_L
#define Clear		FL_BackSpace
#define LeftSoft	FL_F + 1
#define RightSoft	FL_F + 2
#define Green		FL_F + 3
#define Red		FL_F + 4
#define Menu		FL_F + 5
#define Asterisk	FL_F + 6
#define	NumberSign	FL_F + 7
#define Camera		FL_F + 8
#define Dictaphone	FL_F + 9
